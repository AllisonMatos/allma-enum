def detect_waf(url: str) -> dict | None:
    """Detecta WAF em uma URL."""
    import httpx

    try:
        with httpx.Client(timeout=8, verify=False, follow_redirects=True) as client:
            resp = client.get(url, headers={
                "User-Agent": DEFAULT_USER_AGENT
            })

            headers_lower = {k.lower(): v.lower() for k, v in resp.headers.items()}
            cookies = {c.name.lower(): c.value for c in resp.cookies.jar}
            body_lower = resp.text[:5000].lower() if resp.text else ""

            detected = []

            for waf_name, fingerprint in WAF_FINGERPRINTS.items():
                score = 0
                matches = []

                # Check headers
                for h_name, h_patterns in fingerprint.get("headers", {}).items():
                    h_val = headers_lower.get(h_name, "")
                    if h_val:
                        if not h_patterns or h_patterns == [""]:
                            # Header exists = match
                            score += 2
                            matches.append(f"header:{h_name}={h_val[:50]}")
                        else:
                            for p in h_patterns:
                                if p and p in h_val:
                                    score += 3
                                    matches.append(f"header:{h_name}~{p}")

                # Check cookies
                for c_pattern in fingerprint.get("cookies", []):
                    for c_name in cookies:
                        if c_pattern.lower() in c_name:
                            score += 3
                            matches.append(f"cookie:{c_name}")

                # Check body
                for b_pattern in fingerprint.get("body", []):
                    if b_pattern.lower() in body_lower:
                        score += 2
                        matches.append(f"body:~{b_pattern[:30]}")

                # V11: Threshold aumentado para reduzir falsos positivos de headers genéricos
                if score >= 3:
                    detected.append({
                        "waf": waf_name,
                        "confidence": "high" if score >= 5 else "medium" if score >= 3 else "low",
                        "score": score,
                        "matches": matches,
                    })

            if detected:
                # Ordenar por confiança
                detected.sort(key=lambda d: d["score"], reverse=True)
                return {
                    "url": str(resp.url),
                    "waf_detected": detected,
                    "primary_waf": detected[0]["waf"],
                    "status": resp.status_code,
                }

    except Exception:
        pass
    return None


def run(context: dict):
    """Executa detecção de WAF."""
    target = context.get("target")
    if not target:
        raise ValueError("Target required")

    info(
        f"\n🟪───────────────────────────────────────────────────────────🟪\n"
        f"   🛡️ {C.BOLD}{C.CYAN}WAF DETECTION{C.END}\n"
        f"   🎯 Alvo: {C.GREEN}{target}{C.END}\n"
        f"🟪───────────────────────────────────────────────────────────🟪\n"
    )

    outdir = ensure_outdir(target, "waf")

    urls_file = Path("output") / target / "domain" / "urls_valid.txt"
    if not urls_file.exists():
        warn("⚠️ Nenhuma URL válida encontrada.")
        return []

    valid_urls = [l.strip() for l in urls_file.read_text().splitlines() if l.strip()]

    # V11: Deduplicar por hostname + filtrar third-party (hosts que não pertencem ao target)
    seen = set()
    unique_urls = []
    skipped_third_party = 0
    for u in valid_urls:
        parsed = urlparse(u)
        host = parsed.hostname
        if not host:
            continue

        # V11: Filtrar hosts third-party com dot prefix para evitar match parcial
        # (ex: target=oo.com NÃO deve casar com yahoo.com)
        host_lower = host.lower()
        target_lower = target.lower()
        if host_lower != target_lower and not host_lower.endswith("." + target_lower):
            skipped_third_party += 1
            continue

        # Normalizar www. para dedup
        dedup_key = host_lower
        if dedup_key.startswith("www."):
            dedup_key = dedup_key[4:]

        if dedup_key not in seen:
            seen.add(dedup_key)
            # V11: Preservar porta custom para não perder WAFs em portas não-padrão
            netloc = host if not parsed.port or parsed.port in (80, 443) else f"{host}:{parsed.port}"
            unique_urls.append(f"{parsed.scheme}://{netloc}")

    if skipped_third_party:
        info(f"   ℹ️  {skipped_third_party} hosts third-party ignorados (não pertencem a {target})")

    info(f"   📋 Analisando {len(unique_urls)} hosts para WAF...")

    results = []

    with ThreadPoolExecutor(max_workers=15) as executor:
        futures = {executor.submit(detect_waf, url): url for url in unique_urls}

        for future in as_completed(futures):
            try:
                result = future.result()
                if result:
                    results.append(result)
                    waf = result["primary_waf"]
                    conf = result["waf_detected"][0]["confidence"]
                    conf_color = C.RED if conf == "high" else C.YELLOW
                    info(f"   🛡️ {conf_color}[{conf.upper()}]{C.END} {result['url']} → {C.CYAN}{waf}{C.END}")
            except Exception:
                pass

    # Salvar
    output_file = outdir / "waf_results.json"
    output_file.write_text(json.dumps(results, indent=2, ensure_ascii=False))

    if results:
        # Contar WAFs únicos
        waf_counter = {}
        for r in results:
            waf = r["primary_waf"]
            waf_counter[waf] = waf_counter.get(waf, 0) + 1

        success(f"\n   🛡️ WAF detectado em {len(results)}/{len(unique_urls)} hosts!")
        for waf_name, count in sorted(waf_counter.items(), key=lambda x: x[1], reverse=True):
            info(f"   📊 {waf_name}: {count} hosts")
        success(f"   📂 Salvos em {output_file}")
    else:
        info("   ✅ Nenhum WAF detectado (ou não identificável).")

    no_waf = [u for u in unique_urls if u not in {r["url"] for r in results}]
    if no_waf:
        info(f"   🎯 {len(no_waf)} hosts SEM WAF detectado (alvos prioritários)")

    return results
